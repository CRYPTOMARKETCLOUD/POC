$( document ).ready(function() {
    console.log( "ready!" );

    var user_address = web3.eth.defaultAccount;
  
    console.log(user_address);
   
    document.getElementById("#public_address").innerHTML = user_address;

    web3.eth.getBalance(user_address,function(err,res){
        console.log(err,res)

        var ethbal = res / 10 ** 18;
        document.getElementById("#ether_balance").innerHTML = ethbal;
    })   
    
    var tokenContract = web3.eth.contract(contractABI).at(DZOS_Address);

    tokenContract.balanceOf(user_address, (err,token_balance) => {

        console.log(token_balance);
        var token_bal = token_balance / 10 ** 18;
        document.getElementById("#token_balance").innerHTML = token_bal;
    })

   


});

